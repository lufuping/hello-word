#include<iostream>
void oper() {
	// &位运算符(12) 1100 (3) 0011
	std::cout << (12 & 3) << std::endl;
	// &位运算符，清零运算
	std::cout << (12 & 0) << std::endl;
	std::cout << "-------------------------------------------|位运算浮-----------------------------------------------\n";
	// |位运算符(12) 1100 (3) 0011
	std::cout << (12 | 3) << std::endl;
	// |位运算符，置位运算
	std::cout << (12 | 0) << std::endl;
	std::cout << "-------------------------------------------~按位取反运算浮-----------------------------------------------\n";
	std::cout << ~12 << std::endl;
	std::cout << "-------------------------------------------<<右运算浮-----------------------------------------------\n";
	short a = 1, b = -1;
	std::cout << a<<4 <<std::endl;
	std::cout << b<<4 << std::endl;
}
